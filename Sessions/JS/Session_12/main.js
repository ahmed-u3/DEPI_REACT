let day = 5;

if (day === 1) {
    console.log("Saturday");
} else if (day === 2) {
    console.log("Sunday");
} else if (day === 3) {
    console.log("Monday");
} else if (day === 4) {
    console.log("Tuesday");
} else if (day === 5) {
    console.log("Wednesday");
} else if (day === 6) {
    console.log("Thursday");
} else if (day === 7) {
    console.log("Friday");
} else {
    console.log(`${day} not a day.`);
}
